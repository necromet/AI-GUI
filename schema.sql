-- SQLite Schema for AI User Interface Core Interaction Tables

-- 1. Models Table
-- Stores configurations for different AI models used (e.g., 'gemini-2.5-flash').
CREATE TABLE Models (
    model_id INTEGER PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    description TEXT,
    context_window_size INTEGER,
    active BOOLEAN NOT NULL DEFAULT 1,
    -- Additional fields for custom models
    api_key TEXT,
    provider TEXT,
    system_instruction TEXT,
    is_custom BOOLEAN DEFAULT 0
);

